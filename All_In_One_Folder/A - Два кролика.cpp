#include <bits/stdc++.h>
#define ll long long
using namespace std;



void solve(){
    ll x,y,a,b,counter = 1;
    cin>>x>>y>>a>>b;
    counter = (y-x)/(a+b);
    if(x+counter*a == y-counter*b){
        cout<<counter<<endl;
    }else{
        cout<<-1<<endl;
    }

}


int main(){
    ios::sync_with_stdio(false); cin.tie(0);
    long t = 1;
    cin>>t;
    for (int i = 1; i <= t; i++) {
//        cout<<"Case #"<<i<<+": ";
        solve();
    }
}








